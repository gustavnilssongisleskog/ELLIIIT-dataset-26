def format_walks(walks: list[list[tuple[int, int, bool]]], ds: xr.Dataset):
    """Format walks as a list of lists of dicts instedad of a list of lists of tuples.

    Args:
        walks (list[list[tuple[int, int, bool]]]): Walks as generated by `generate_walks`
        ds (xr.Dataset): Dataset

    Returns:
        list[list[dict]]: Instead of tuples, uses dicts for the information. Also includes the rover position as one numpy ndarray. 
    """
    walks_formatted = []
    for walk in walks:
        walk_info = []
        for exp, cycle, rest in walk:
            point = np.array([ds.data_vars["rover_x"].data[exp, cycle],
                ds.data_vars["rover_y"].data[exp, cycle],
                ds.data_vars["rover_z"].data[exp, cycle]])
            walk_info.append({
                "experiment_id": exp,
                "cycle_id": cycle,
                "position": point,
                "resting": rest
            })
        walks_formatted.append(walk_info)
    return walks_formatted